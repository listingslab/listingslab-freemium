<h3 id="multisite_browser"><strong>MultiSite Browser</strong> - <a href="#toc">top</a></h3>
	<p>Provides functionality to easily find, navigate and link to all websites on a WordPress MultiSite Network. If a WordPress site is website is part of a MultiSite Network, so we can use the <strong>[_multisite]</strong> shortcode to embed a jQuery powered browser which makes it easy to find what you're looking for on large networks. If the shortcode is used on a single installation, the user will see a message telling them how to upgrade to a network.</p>

	<p>Everyone loves an icon. upload a blog icon 150px square. Why a square icon? Images can be portrait, landscape or square. Their dimension ratio is always different. Unless it's square. Squares are the only kind of images which fit nicely into lists If you've got 100 images like that. This is why your Facebook icon is square.</p>
